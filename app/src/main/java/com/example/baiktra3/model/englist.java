package com.example.baiktra3.model;

public class englist {
    String thi, vidu;

    public englist() {
    }

    public englist(String thi, String vidu) {
        this.thi = thi;
        this.vidu = vidu;
    }

    public String getThi() {
        return thi;
    }

    public void setThi(String thi) {
        this.thi = thi;
    }

    public String getVidu() {
        return vidu;
    }

    public void setVidu(String vidu) {
        this.vidu = vidu;
    }
}
